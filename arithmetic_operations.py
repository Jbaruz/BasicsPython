# Declare variables
a = 100
b = 2
c = 25
d = 4

# Perform arithmetic operations and display results
print("a - b =", a - b)  # Subtraction
print("b * c =", b * c)  # Multiplication
print("a / c =", a // c)  # Integer division
print("a + b * c =", a + b * c)  # Operator precedence
print("a * b + c * d =", a * b + c * d)

# Output:
# a - b = 98
# b * c = 50
# a / c = 4
# a + b * c = 150
# a * b + c * d = 300
